#CONDITIONING IN PYTHON

#Conditioning is used to perform different actions based on different conditions
#Python has the following conditional statements:

#If statement
#If statement is used to execute a block of code if a condition is true
if "condition":
    print("Condition is true")

#If..else statement
#If..else statement is used to execute a block of code if a condition is true and another block of code if the condition is false
if "condition":
    print("Condition is true")
else:
    print("Condition is false")

#If..elif..else statement
#If..elif..else statement is used to execute a block of code if the first condition is true, if the first condition is false, then it checks the next condition and so on
if "condition":
    print("Condition is true")
elif "another condition":
    print("Another condition is true")
else:
    print("All conditions are false")


#Nested if statement
#Nested if statement is used to check another condition after a condition is true
if "condition":
    if "another condition":
        print("Another condition is true")
    else:
        print("Another condition is false")
else:
    print("Condition is false")



